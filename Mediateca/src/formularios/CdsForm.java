    private Object[][] convertirAFilasDeTabla(List<Cd> cds) {
        Object[][] matriz = new Object[cds.size()][7];

        for (int i = 0; i < cds.size(); i++) {
            Cd cd = cds.get(i);
            matriz[i][0] = cd.getCodigo();
            matriz[i][1] = cd.getTitulo();
            matriz[i][2] = cd.getArtista();
            matriz[i][3] = cd.getGenero();
            matriz[i][4] = cd.getDuracion();
            matriz[i][5] = cd.getNumCanciones();
            matriz[i][6] = cd.getUnidadesDisp();
        }

        return matriz;
    }
    
    private void pintarTabla() {
        CdDao cdDao = new CdDao();
        
        try {
            List<Cd> cds = cdDao.obtenerCds();
            Object[][] filasDeTabla = convertirAFilasDeTabla(cds);
            String[] titulosTabla = new String[] {"Codigo", "Titulo", "Artista", 
                "Genero", "Duracion", "Num Canciones", "Unidades Disp"};

            DefaultTableModel model = new DefaultTableModel(filasDeTabla, titulosTabla);
            tblCds.setModel(model);
        } catch (Exception e) {
            e.printStackTrace();
        }
        
    }

